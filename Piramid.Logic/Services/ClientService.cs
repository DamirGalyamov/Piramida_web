using Microsoft.EntityFrameworkCore;
using Piramid.Logic.Interfaces.Services;
using Piramida.Logic.DtoModels.Filters;
using Piramida.Storage.Database;
using Piramida.Storage.Models;

namespace Piramid.Logic.Services
{
    public class ClientService : IClientService
    {
        public IQueryable<Client> GetClientQueryable(DataContext dataContext, ClientFilterDto filter, bool AsNoTraking)
        {
            IQueryable<Client> query = dataContext.Clients;

            if (AsNoTraking)
            {
                query = query.AsNoTracking();
            }
            if (filter != null)
            {
                query = query.Where(x => x.Id == filter.Id);
            }
            return query;
        }
    }
}
