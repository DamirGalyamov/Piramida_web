using Microsoft.EntityFrameworkCore;
using Piramid.Logic.Interfaces.Services;
using Piramida.Logic.DtoModels.Filters;
using Piramida.Storage.Database;
using Piramida.Storage.Models;

namespace Piramid.Logic.Services
{
    public class EmployeeService : IEmployeeService
    {
        public IQueryable<Employee> GetEmployeeQueryable(DataContext dataContext, EmployeeFilterDto filter, bool AsNoTraking)
        {
            IQueryable<Employee> query = dataContext.Employees;

            if (AsNoTraking)
            {
                query = query.AsNoTracking();
            }
            if (filter != null)
            {
                query = query.Where(x => x.Id == filter.Id);
            }
            return query;
        }
    }
}
